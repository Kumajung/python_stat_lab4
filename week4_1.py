""" Hypothesis Testing หน้า 10
ตัวอย่าง สมมติฐาน การทดสอบทางเดียว และ การทดสอบสองทาง 
นักวิจัยมีผลลัพธ์สําหรับกลุ่มตัวอย่างของนักเรียนที่สอบระดับชาติที่โรงเรียนมัธยมศึกษาตอนปลาย 
นักวิจัยต้องการทราบว่าคะแนนของโรงเรียนนั้นแตกต่างจากค่าเฉลี่ยของประเทศที่ 850 หรือไม่ 
สมมติฐานนี้เป็นการทดสอบสองทางหรือการทดสอบทางเดียว? 
สมมติฐานการทดสอบสองทางมีความเหมาะสมเนื่องจากผู้วิจัยสนใจ
ที่จะพิจารณาว่าคะแนนน้อยกว่าหรือมากกว่าค่าเฉลี่ยของประเทศ (H0: μ = 850 เทียบกับ H1: μ≠ 850)
"""
import numpy as np
from scipy.stats import ttest_1samp

# ข้อมูลตัวอย่างคะแนนของนักเรียนในโรงเรียน เป็นการจำลองข้อมูลเพื่อให้เห็นภาพชัดจากการอธิบายในหน้า 10
school_scores = [860, 845, 870, 855, 840, 875, 860, 835, 850, 865]

# ค่าเฉลี่ยระดับประเทศ
national_mean = 850

# การทดสอบสมมติฐาน (Two-sided t-test)
# H0: μ = 850 (ค่าเฉลี่ยเท่ากับค่าเฉลี่ยระดับประเทศ)
# H1: μ ≠ 850 (ค่าเฉลี่ยไม่เท่ากับค่าเฉลี่ยระดับประเทศ)

# คำนวณค่า t-test และค่า p-value
t_stat, p_value = ttest_1samp(school_scores, national_mean)

# แสดงผลลัพธ์
print("ค่า t-statistic:", round(t_stat,5))
print("ค่า p-value:", round(p_value,5))

# กำหนดระดับนัยสำคัญ (alpha)
alpha = 0.05

# สรุปผลการทดสอบ
if p_value < alpha:
    print("ปฏิเสธสมมติฐานว่าง (Reject H0): ค่าเฉลี่ยของโรงเรียนแตกต่างจากค่าเฉลี่ยระดับประเทศ")
else:
    print("ไม่สามารถปฏิเสธสมมติฐานว่าง (Fail to Reject H0): ค่าเฉลี่ยของโรงเรียนไม่แตกต่างจากค่าเฉลี่ยระดับประเทศ")
